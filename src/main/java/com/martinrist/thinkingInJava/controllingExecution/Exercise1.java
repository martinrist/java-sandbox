package com.martinrist.thinkingInJava.controllingExecution;

/**
 * Thinking in Java - 'Controlling Execution' Chapter
 * Exercise 1 - Print values from 1 to 100
 */
public class Exercise1 {

    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
    }

}
